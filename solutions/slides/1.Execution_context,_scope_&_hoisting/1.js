/* 3 ways of creating a variable: var, let and const. */

/* Declaration */
var myVariable;
let myVariable2;

console.log(myVariable);
console.log(myVariable2);

/* Initialization */
myVariable = 'Hello';
myVariable2 = 'Hello2';

console.log(myVariable);
console.log(myVariable2);

